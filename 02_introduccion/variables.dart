void main() {
  //String
  final String name = "nombre";
  print(name);

  //Int
  final int num = 2;
  print(num);

  //double
  final double flo = 0.25;
  print(flo);

  //boolean
  final bool ask = true;
  print(ask);

  //array
  final tienda = [name, num, flo, ask];
  print(tienda);

  //array especifico
  final List<String> prod = [name, "esferos", "hojas"];
  print(prod);
}
